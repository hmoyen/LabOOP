
public class Endereco {

    String logradouro;
    int numero;
    String complemento;
    long CEP;
    String cidade;
    String estado;
    int moradores;

    public Endereco(String logradouro, int numero, String complemento, long CEP, String cidade, String estado, int moradores) {
        this.logradouro = logradouro;
        this.numero = numero;
        this.complemento = complemento;
        this.CEP = CEP;
        this.cidade = cidade;
        this.estado = estado;
        this.moradores = moradores;
    }

    
    
}